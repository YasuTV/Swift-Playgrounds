var gemCounter = 0
var switchCounter = 0

while gemCounter == 0 || gemCounter != switchCounter {
    if isBlocked{
        turnRight()
    }
    
    moveForward()
    
    if isOnGem {
        collectGem()
        gemCounter += 1
    }
    
    if isOnClosedSwitch {
        toggleSwitch()
        switchCounter += 1
    }
}
